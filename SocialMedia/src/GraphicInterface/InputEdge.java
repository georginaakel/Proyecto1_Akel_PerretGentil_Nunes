    public void addRelation(){
        try{       
            start = graph.findVnum(startInput.getText());
            end = graph.findVnum(endInput.getText());
            weight = Integer.parseInt(weightInput.getText());

            graph.addEdge(start, end, weight);       
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error: dato ingresado no valido");
        }
    }

